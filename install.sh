#!/usr/bin/env bash
# Run from the cloned benchmark repo. Clones MMORE + creates venv + env.benchmark.
#
#   cd /workspace
#   git clone https://github.com/chadiaitekioui/mmore_retrieval_benchmark.git
#   cd mmore_retrieval_benchmark
#   bash install.sh
#   source env.benchmark
#
# Optional env:
#   WORKDIR         parent of this repo (default: parent of BENCH_ROOT)
#   MMORE_GIT_URL   default: https://github.com/chadiaitekioui/mmore.git (fork until PR #312 merges)
#   MMORE_GIT_BRANCH  default: llm-as-a-judge
#   MMORE_DEVICE         cpu (default) or cu126 for GPU torch on process/index
#   INSTALL_HF_ANNOTATE  1 = also pip install -r requirements-hf-annotate.txt (default: 0)

set -euo pipefail

BENCH_ROOT="$(cd "$(dirname "$0")" && pwd)"
WORKDIR="${WORKDIR:-$(cd "${BENCH_ROOT}/.." && pwd)}"
WORKDIR="$(mkdir -p "$WORKDIR" && cd "$WORKDIR" && pwd)"

MMORE_ROOT="${WORKDIR}/mmore"
MMORE_GIT_URL="${MMORE_GIT_URL:-https://github.com/chadiaitekioui/mmore.git}"
MMORE_GIT_BRANCH="${MMORE_GIT_BRANCH:-llm-as-a-judge}"

clone_mmore() {
  if [[ -d "${MMORE_ROOT}/.git" ]]; then
    echo "  exists: ${MMORE_ROOT}"
    return 0
  fi
  if [[ -e "$MMORE_ROOT" ]]; then
    echo "Not a git repo: ${MMORE_ROOT}" >&2
    exit 1
  fi
  echo "  clone ${MMORE_GIT_URL} @ ${MMORE_GIT_BRANCH} → ${MMORE_ROOT}"
  git clone -b "$MMORE_GIT_BRANCH" -- "$MMORE_GIT_URL" "$MMORE_ROOT"
}

echo "=== MMORE retrieval benchmark — install ==="
echo "WORKDIR=${WORKDIR}"
echo "BENCH_ROOT=${BENCH_ROOT}"

command -v git >/dev/null || { echo "git is required" >&2; exit 1; }
command -v python3 >/dev/null || { echo "python3 is required" >&2; exit 1; }

echo "=== 1/3 Clone MMORE (sibling) ==="
clone_mmore
[[ -d "${MMORE_ROOT}/src/mmore" ]] || { echo "Bad MMORE checkout: ${MMORE_ROOT}" >&2; exit 1; }

HF_HOME="${HF_HOME:-${WORKDIR}/hf_cache}"
DB_URI="${DB_URI:-${BENCH_ROOT}/proc_demo.db}"
DB_NAME="${DB_NAME:-my_db}"
COLLECTION_NAME="${COLLECTION_NAME:-my_db}"
mkdir -p "$HF_HOME" "$(dirname "$DB_URI")"

ENV_FILE="${BENCH_ROOT}/env.benchmark"
cat >"$ENV_FILE" <<EOF
# Generated by install.sh — source from BENCH_ROOT: source env.benchmark

export WORKDIR="${WORKDIR}"
export BENCH_ROOT="${BENCH_ROOT}"
export MMORE_ROOT="${MMORE_ROOT}"
export HF_HOME="${HF_HOME}"
export DB_URI="${DB_URI}"
export DB_NAME="${DB_NAME}"
export COLLECTION_NAME="${COLLECTION_NAME}"

if [[ -f "\${BENCH_ROOT}/.venv/bin/activate" ]]; then
  # shellcheck disable=SC1091
  source "\${BENCH_ROOT}/.venv/bin/activate"
fi
EOF

echo "=== 2/3 Python venv ==="
cd "$BENCH_ROOT"
if [[ ! -d .venv ]]; then
  python3 -m venv .venv
fi
# shellcheck disable=SC1091
source .venv/bin/activate

INSTALL_HF_ANNOTATE="${INSTALL_HF_ANNOTATE:-0}"

echo "=== 3/3 pip install ==="
pip install -U pip -q
pip install -r requirements.txt
if [[ "$INSTALL_HF_ANNOTATE" == "1" ]]; then
  echo "  + requirements-hf-annotate.txt (INSTALL_HF_ANNOTATE=1)"
  pip install -r requirements-hf-annotate.txt
fi
pip install -r corpus/requirements.txt
MMORE_DEVICE="${MMORE_DEVICE:-cpu}"   # or cu126 for GPU torch
pip install -e "${MMORE_ROOT}[process,${MMORE_DEVICE}]"   # process + postprocess
pip install -e "${MMORE_ROOT}[index,${MMORE_DEVICE}]"
pip install -e "${MMORE_ROOT}[rag,api,websearch]"   # websearch = ddgs for judge ADD_CONTEXT

echo ""
echo "Installation complete."
echo ""